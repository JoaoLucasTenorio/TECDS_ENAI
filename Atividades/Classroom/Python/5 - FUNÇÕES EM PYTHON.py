def soma_quadrados(lista_numeros):
    soma = 0
    for numero in lista_numeros:
        soma += numero ** 2
    return soma

numeros = [1, 2, 3, 4, 5]
resultado = soma_quadrados(numeros)
print(f"A soma dos quadrados dos números é: {resultado}")

