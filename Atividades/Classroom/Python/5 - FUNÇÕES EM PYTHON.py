def verifica_primo(numero):
    if numero < 2:
        return False
    for i in range(2, int(numero**0.5) + 1):
        if numero % i == 0:
            return False
    return True

numero_usuario_primo = int(input("Digite um número para verificar se é primo: "))

if verifica_primo(numero_usuario_primo):
    print(f"{numero_usuario_primo} é um número primo.")
else:
    print(f"{numero_usuario_primo} não é um número primo.")

