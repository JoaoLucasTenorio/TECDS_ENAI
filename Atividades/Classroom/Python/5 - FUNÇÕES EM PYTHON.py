def conta_vogais(frase):
    vogais = "aeiouAEIOU"
    contador = 0

    for char in frase:
        if char in vogais:
            contador += 1

    return contador

frase_usuario = input("Digite uma frase: ")
numero_vogais = conta_vogais(frase_usuario)

print(f"O número de vogais na frase é: {numero_vogais}")

